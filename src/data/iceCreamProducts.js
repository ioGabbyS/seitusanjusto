export const ICE_CREAM_PRODUCTS = Object.entries(ICE_CREAM_CATALOG).flatMap(([cat, items]) =>
    items.map((item) => {
        let targetCategory = "IMPULSIVOS";
        const catUpper = cat.toUpperCase();

        if (catUpper.includes("BALDE") || catUpper.includes("POTE")) targetCategory = "BALDES Y POTES";
        else if (catUpper.includes("TORTA")) targetCategory = "TORTAS";
        else if (catUpper.includes("CORMILLOT")) targetCategory = "LIBRES DE GLUTEN";
        else if (catUpper.includes("HOGAREN")) targetCategory = "LINEA HOGAREÑA";

        return {
            id: item.id?.toString() || `ice-legacy-${item.name.replace(/\s+/g, '-')}`, // USE FIXED ID
            name: item.name.toUpperCase(),
            category: targetCategory,
            price: item.price || 0,
            quantity: 0,
            minStock: 5,
            cc: 0,
            packUnits: item.pack,
            barcode: item.barcode || "",
            pointsEarnedRatio: item.ratioPts || 1
        };
    })
);

